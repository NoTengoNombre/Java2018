public class OperadoresAritmeticos {

 public static void main(String[] args) {

  int n1 = 2, n2;
  n2 = n1 * n1;
  System.out.println("N2 : " + n2);
  n2 = n2 - n1;
  System.out.println("N2 : " + n2);
  n2 = n2 + n1 + 15;
  System.out.println("N2 : " + n2);
  n2 = n2 / n1;
  System.out.println("N2 : " + n2);
  n2 = n2 % n1;
  System.out.println("N2 : " + n2);

 }

}
